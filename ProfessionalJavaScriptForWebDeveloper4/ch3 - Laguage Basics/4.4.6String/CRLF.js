// 回车换行符号 \n 与\r
// \r 回车将光标移动到本行开头
console.log("---\\r---");
console.log("hello \r world");
console.log("---\\n---");
console.log("hello \n world");
console.log("---\\r\\n---");
console.log("hello \n\rworld");
